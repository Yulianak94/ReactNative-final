class Product {
    constructor(
      id,
      departmentIds,
      picture,
      title,
      description,
      size,
      shipping,
      price,
      reviews,
      quantity
    ) {
      this.id = id;
      this.departmentIds = departmentIds;
      this.picture = picture;
      this.title = title;
      this.title = title;
      this.description = description;
      this.size = size;
      this.shipping = shipping;
      this.price = price;
      this.reviews = reviews;
      this.quantity=quantity;
    }
  }
  
export default Product;